import java.util.*;

public class Main
{
    public static void main(String[] args)
    {
        int a=90;
        double s=2.435;
        
        double sa=(double)a;
        int as=(int)s;
        
        System.out.println("The original integer value is: "+a);
        System.out.println("The original double value is: "+s);
        
        System.out.println("The modified double value is: "+sa);
        System.out.println("The modified integer value is: "+as);
    }
}
